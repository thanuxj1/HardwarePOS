export function t(key, params = {}) {
  const lang = (window.__TF_LANG__ || 'en');
  const dict = translations[lang] || translations.en;
  let str = dict[key] || translations.en[key] || key;
  Object.entries(params).forEach(([k, v]) => { str = str.replace(`{{${k}}}`, v); });
  return str;
}

export function setLanguage(lang) {
  window.__TF_LANG__ = lang;
  localStorage.setItem('tf_language', lang);
  document.documentElement.lang = lang === 'si' ? 'si' : 'en';
  // Dispatch event so components can re-render
  window.dispatchEvent(new CustomEvent('tf-language-change', { detail: { lang } }));
}

export function initLanguage() {
  const saved = localStorage.getItem('tf_language') || 'en';
  window.__TF_LANG__ = saved;
  document.documentElement.lang = saved === 'si' ? 'si' : 'en';
}
